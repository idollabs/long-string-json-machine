const string = `Hi there! I'm super excited to share with you some JSONified text generated by my "Large String JSON Machine." This technology is the future of converting Books to JSON. I look forward to you experiencing this innovation on my upcoming web application, "Typing Classics!"`;
console.log(string);

let preJsonArr = [];

for (let i = 0; i < string.length; i++) {
  let arrObj = {};
  arrObj.id = i;
  arrObj.char = string[i];
  preJsonArr.push(arrObj);
}

const bookText = JSON.stringify(preJsonArr);

console.log(bookText);
